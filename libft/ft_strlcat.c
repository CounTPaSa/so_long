#include "libft.h"

size_t	ft_strlcat(char	*dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	len_dst;
	size_t	len_src;

	len_dst = ft_strlen(dst);
	len_src = ft_strlen(src);
	if (size <= len_dst)
		return (size + len_src);
	i = -1;
	j = len_dst;
	while (src[++i] && j < size - 1)
		dst[j++] = src[i];
	return (dst[j] = 0, len_dst + len_src);
}
